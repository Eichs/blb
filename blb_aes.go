// keys[0..15] = key[Blb3AESShift[i]]
// offset=0x1f; for round 0..9: 生成后续 160 bytes
func blbAESExpand(key []byte) []byte {
	if len(key) != 16 {
		panic("blbAESExpand: key must be 16 bytes")
	}
	keys := make([]byte, 176)

	for i := 0; i < 16; i++ {
		keys[i] = key[blb3AESShift[i]]
	}

	offset := 0x1f
	for round := 0; round < 10; round++ {
		a := blb3AESSBox[keys[offset-0x14]]
		b := blb3AESSBox[keys[offset-0x10]]
		c := blb3AESSBox[keys[offset-0x18]] ^ keys[offset-0x18] ^ powerSchedule[round] ^ keys[offset-0x1f]
		d := blb3AESSBox[keys[offset-0x1c]]
		var temp byte

		keys[offset-0x0f] = c
		temp = a ^ keys[offset-0x14] ^ keys[offset-0x1b]
		keys[offset-0x0b] = temp
		a = b ^ keys[offset-0x10] ^ keys[offset-0x17]
		keys[offset-0x07] = a
		b = d ^ keys[offset-0x1c] ^ keys[offset-0x13]
		keys[offset-0x03] = b
		c = c ^ keys[offset-0x1e]
		keys[offset-0x0e] = c
		temp = temp ^ keys[offset-0x1a]
		keys[offset-0x0a] = temp
		a = a ^ keys[offset-0x16]
		keys[offset-0x06] = a
		b = b ^ keys[offset-0x12]
		keys[offset-0x02] = b
		c = c ^ keys[offset-0x1d]
		keys[offset-0x0d] = c
		temp = temp ^ keys[offset-0x19]
		keys[offset-0x09] = temp
		a = a ^ keys[offset-0x15]
		keys[offset-0x05] = a
		b = b ^ keys[offset-0x11]
		keys[offset-0x01] = b
		keys[offset-0x0c] = c ^ keys[offset-0x1c]
		keys[offset-0x08] = temp ^ keys[offset-0x18]
		keys[offset-0x04] = a ^ keys[offset-0x14]
		keys[offset] = b ^ keys[offset-0x10]

		offset += 0x10
	}

	return keys
}

// 10 轮：前 9 轮有 MixCols，最后一轮无 MixCols
func BlbAESEncrypt(m []byte, k []byte) []byte {
	if len(m) != 16 || len(k) != 16 {
		panic("BlbAESEncrypt: m and k must be 16 bytes")
	}

	keys := blbAESExpand(k)

	c := make([]byte, 16)
	copy(c, m)

	xorRoundKey(c, keys, 0)

	for i := 0; i < 9; i++ {
		subBytes(c)
		shiftRows(c)
		mixCols(c)
		xorRoundKey(c, keys, i+1)
	}

	subBytes(c)
	shiftRows(c)
	xorRoundKey(c, keys, 10)

	return c
}

func subBytes(a []byte) {
	for i := 0; i < len(a); i++ {
		// XOR substitution和标准 AES 不同
		a[i] ^= blb3AESSBox[a[i]]
	}
}

func xorRoundKey(state []byte, keys []byte, round int) {
	base := round * 16
	for i := 0; i < 4; i++ {
		for j := 0; j < 4; j++ {
			state[i*4+j] ^= keys[i+j*4+base]
		}
	}
}

func shiftRows(state []byte) {
	var temp [16]byte
	copy(temp[:], state[:16])
	for i := 0; i < 16; i++ {
		state[i] = temp[shiftRowsTable[i]]
	}
}

func mixCol(state []byte, off int) {
	a0 := state[off+0]
	a1 := state[off+1]
	a2 := state[off+2]
	a3 := state[off+3]

	state[off+0] = lookupG2[a0] ^ lookupG3[a1] ^ a2 ^ a3
	state[off+1] = lookupG2[a1] ^ lookupG3[a2] ^ a3 ^ a0
	state[off+2] = lookupG2[a2] ^ lookupG3[a3] ^ a0 ^ a1
	state[off+3] = lookupG2[a3] ^ lookupG3[a0] ^ a1 ^ a2
}

func mixCols(state []byte) {
	mixCol(state, 0)
	mixCol(state, 4)
	mixCol(state, 8)
	mixCol(state, 12)
}
func BlbAESDecrypt(c []byte, k []byte) []byte {
	if len(c) != 16 || len(k) != 16 {
		panic("BlbAESDecrypt: c and k must be 16 bytes")
	}

	keys := blbAESExpand(k)
	m := make([]byte, 16)
	copy(m, c)

	xorRoundKey(m, keys, 10)
	invShiftRows(m)
	invSubBytes(m)

	for i := 9; i > 0; i-- {
		xorRoundKey(m, keys, i)
		invMixCols(m)
		invShiftRows(m)
		invSubBytes(m)
	}

	xorRoundKey(m, keys, 0)

	return m
}

func invSubBytes(a []byte) {

	var invSBox [256]byte
	for i := 0; i < 256; i++ {

		output := byte(i) ^ blb3AESSBox[i]

		invSBox[output] = byte(i)
	}

	for i := 0; i < len(a); i++ {
		a[i] = invSBox[a[i]]
	}
}

func invShiftRows(state []byte) {
	var temp [16]byte
	copy(temp[:], state[:16])

	var invTable [16]byte
	for i := 0; i < 16; i++ {
		invTable[shiftRowsTable[i]] = byte(i)
	}

	for i := 0; i < 16; i++ {
		state[i] = temp[invTable[i]]
	}
}

func invMixCol(state []byte, off int) {
	a0 := state[off+0]
	a1 := state[off+1]
	a2 := state[off+2]
	a3 := state[off+3]

	state[off+0] = lookupG14[a0] ^ lookupG11[a1] ^ lookupG13[a2] ^ lookupG9[a3]
	state[off+1] = lookupG9[a0] ^ lookupG14[a1] ^ lookupG11[a2] ^ lookupG13[a3]
	state[off+2] = lookupG13[a0] ^ lookupG9[a1] ^ lookupG14[a2] ^ lookupG11[a3]
	state[off+3] = lookupG11[a0] ^ lookupG13[a1] ^ lookupG9[a2] ^ lookupG14[a3]
}

func invMixCols(state []byte) {
	invMixCol(state, 0)
	invMixCol(state, 4)
	invMixCol(state, 8)
	invMixCol(state, 12)
}

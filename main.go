func CompareHeaderPlain(nameA string, a []byte, nameB string, b []byte, maxDiffs int, ctx int) {
	fmt.Printf("==== HEADER PLAIN DIFF ====\n")
	fmt.Printf("A=%s len=%d (0x%X)\n", nameA, len(a), len(a))
	fmt.Printf("B=%s len=%d (0x%X)\n", nameB, len(b), len(b))

	min := len(a)
	if len(b) < min {
		min = len(b)
	}

	diffCount := 0
	first := true

	for i := 0; i < min; i++ {
		if a[i] == b[i] {
			continue
		}
		diffCount++
		if first {
			first = false
			fmt.Printf("FirstDiff @0x%X (%d): A=%02X B=%02X\n", i, i, a[i], b[i])
		}

		if maxDiffs > 0 && diffCount <= maxDiffs {
			fmt.Printf("Diff #%d @0x%X: A=%02X  B=%02X\n", diffCount, i, a[i], b[i])
			// 打印附近 ctx 字节窗口
			start := i - ctx
			if start < 0 {
				start = 0
			}
			end := i + ctx + 1
			if end > min {
				end = min
			}
			fmt.Printf("  A[%#x:%#x]=%s\n", start, end, hexSpace(a[start:end]))
			fmt.Printf("  B[%#x:%#x]=%s\n", start, end, hexSpace(b[start:end]))
		}
		if maxDiffs > 0 && diffCount == maxDiffs {
			fmt.Printf("... (reached maxDiffs=%d, stop printing more)\n", maxDiffs)
			break
		}
	}

	// 长度差
	if len(a) != len(b) {
		fmt.Printf("LengthMismatch: A=%d B=%d (commonCompared=%d)\n", len(a), len(b), min)
		// 如果你想看尾部额外内容：
		if len(a) > min {
			tail := a[min:]
			n := len(tail)
			if n > 64 {
				n = 64
			}
			fmt.Printf("A extra tail [0:%d]=%s\n", n, hexSpace(tail[:n]))
		}
		if len(b) > min {
			tail := b[min:]
			n := len(tail)
			if n > 64 {
				n = 64
			}
			fmt.Printf("B extra tail [0:%d]=%s\n", n, hexSpace(tail[:n]))
		}
	}

	// 如果 maxDiffs==0 或 diffCount 没有打印完，我们也给个总计
	if maxDiffs <= 0 || diffCount <= maxDiffs {
		fmt.Printf("TotalDiffBytes=%d (compared %d bytes)\n", diffCount, min)
	} else {
		fmt.Printf("TotalDiffBytes>=%d (stopped early)\n", diffCount)
	}
	fmt.Printf("==== END HEADER DIFF ====\n")
}

// 主入口示例：对比 mihoyo_sdk_res 和 out
func DiffTwoBlb3Headers(fileA, fileB string) error {
	outerA, keyA, encA, plainA, err := ReadAndDecryptHeaderBytes(fileA)
	if err != nil {
		return err
	}
	outerB, keyB, encB, plainB, err := ReadAndDecryptHeaderBytes(fileB)
	if err != nil {
		return err
	}

	fmt.Printf("==== BLB3 HEADER META ====\n")
	fmt.Printf("A=%s outerFlag=0x%08X headerKey=%s blocksInfoSize=%d\n",
		fileA, outerA, strings.ToUpper(hex.EncodeToString(keyA)), len(encA))
	fmt.Printf("B=%s outerFlag=0x%08X headerKey=%s blocksInfoSize=%d\n",
		fileB, outerB, strings.ToUpper(hex.EncodeToString(keyB)), len(encB))
	fmt.Printf("==== END META ====\n\n")

	// 关键：对比“解密后的 headerBytes”
	CompareHeaderPlain(fileA, plainA, fileB, plainB /*maxDiffs=*/, 200 /*ctx=*/, 16)

	return nil
}

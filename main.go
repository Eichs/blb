func DumpBlb3File(b *Blb3File, headerBytesPreview int, maxNodesPrint int) {
	fmt.Printf("==== BLB3 FULL DUMP ====\n")

	// ---- basic info ----
	fmt.Printf("Offset               = %d (0x%X)\n", b.Offset, uint64(b.Offset))

	// ---- header key ----
	if len(b.HeaderKey) > 0 {
		fmt.Printf("HeaderKey            = %d bytes\n", len(b.HeaderKey))
		fmt.Printf("HeaderKeyHex         = %s\n", strings.ToUpper(hex.EncodeToString(b.HeaderKey)))
	} else {
		fmt.Printf("HeaderKey            = <nil>\n")
	}

	// ---- header bytes ----
	if len(b.HeaderBytes) > 0 {
		fmt.Printf("HeaderBytesLen       = %d\n", len(b.HeaderBytes))
		n := headerBytesPreview
		if n > len(b.HeaderBytes) {
			n = len(b.HeaderBytes)
		}
		fmt.Printf("HeaderBytes[0:%d]     = %s\n", n, hexSpace(b.HeaderBytes[:n]))
	} else {
		fmt.Printf("HeaderBytes          = <nil>\n")
	}

	// ---- header fields ----
	if b.Header != nil {
		h := b.Header
		fmt.Printf("---- Blb3 HeaderBytes Fields ----\n")
		fmt.Printf("HeaderSize            = %d (0x%X)\n", h.HeaderSize, h.HeaderSize)
		fmt.Printf("LastUncompressedSize  = %d (0x%X)\n", h.LastUncompressedSize, h.LastUncompressedSize)
		fmt.Printf("BlobOffset            = %d (0x%X)\n", h.BlobOffset, uint32(h.BlobOffset))
		fmt.Printf("BlobSize              = %d (0x%X)\n", h.BlobSize, h.BlobSize)
		fmt.Printf("CompressionType       = %d\n", h.CompressionType)
		fmt.Printf("BlockPow              = %d\n", h.BlockPow)
		fmt.Printf("BlockSize             = %d (0x%X)\n", h.BlockSize, h.BlockSize)
		fmt.Printf("BlocksInfoCount       = %d\n", h.BlocksInfoCount)
		fmt.Printf("NodesCount            = %d\n", h.NodesCount)
		fmt.Printf("BlocksInfoOffsetAbs   = %d (0x%X)\n", h.BlocksInfoOffsetAbs, uint64(h.BlocksInfoOffsetAbs))
		fmt.Printf("NodesInfoOffsetAbs    = %d (0x%X)\n", h.NodesInfoOffsetAbs, uint64(h.NodesInfoOffsetAbs))
		fmt.Printf("FlagInfoOffsetAbs     = %d (0x%X)\n", h.FlagInfoOffsetAbs, uint64(h.FlagInfoOffsetAbs))
		fmt.Printf("-------------------------------\n")
	} else {
		fmt.Printf("Header               = <nil>\n")
	}

	// ---- blocks ----
	fmt.Printf("---- Blocks (%d) ----\n", len(b.Blocks))
	for i, blk := range b.Blocks {
		comp := CompressionType(blk.Flags & StorageBlockFlags(0x3f)) // 如果你有 mask 就换成你的
		fmt.Printf("[%03d] C=%d (0x%X)  U=%d (0x%X)  Flags=0x%X  Comp=%d\n",
			i,
			blk.CompressedSize, blk.CompressedSize,
			blk.UncompressedSize, blk.UncompressedSize,
			uint32(blk.Flags),
			comp,
		)
		if i == 15 && len(b.Blocks) > 16 {
			fmt.Printf("... (%d more blocks)\n", len(b.Blocks)-16)
			break
		}
	}
	fmt.Printf("----------------------\n")

	// ---- blocks stream ----
	if len(b.BlocksStream) > 0 {
		fmt.Printf("BlocksStreamLen      = %d (0x%X)\n", len(b.BlocksStream), len(b.BlocksStream))
	} else {
		fmt.Printf("BlocksStream         = <nil>\n")
	}

	// ---- nodes ----
	fmt.Printf("---- Nodes (%d) ----\n", len(b.Nodes))
	limit := len(b.Nodes)
	if maxNodesPrint > 0 && limit > maxNodesPrint {
		limit = maxNodesPrint
	}
	for i := 0; i < limit; i++ {
		n := b.Nodes[i]
		fmt.Printf("[%03d] Off=%d (0x%X)  Size=%d (0x%X)  Flags=0x%X  Path=%q\n",
			i,
			n.Offset, uint32(n.Offset),
			n.Size, uint32(n.Size),
			n.Flags,
			n.Path,
		)
	}
	if limit < len(b.Nodes) {
		fmt.Printf("... (%d more nodes)\n", len(b.Nodes)-limit)
	}
	fmt.Printf("----------------------\n")

	fmt.Printf("==== END DUMP ====\n")
}

func hexSpace(b []byte) string {
	const perLine = 16
	var sb strings.Builder
	for i := 0; i < len(b); i++ {
		if i > 0 {
			if i%perLine == 0 {
				sb.WriteString(" | ")
			} else {
				sb.WriteByte(' ')
			}
		}
		sb.WriteString(strings.ToUpper(hex.EncodeToString(b[i : i+1])))
	}
	return sb.String()
}

// ReadAndDecryptHeaderBytes：直接从文件按格式读 headerEnc，然后用 headerKey 解密，返回 headerPlain
func ReadAndDecryptHeaderBytes(path string) (outerFlag uint32, headerKey []byte, headerEnc []byte, headerPlain []byte, err error) {
	f, err := os.Open(path)
	if err != nil {
		return 0, nil, nil, nil, err
	}
	defer f.Close()

	br := newBinReader(f)

	sig, err := br.ReadStringToNullFixed(4)
	if err != nil {
		return 0, nil, nil, nil, err
	}
	if sig != "Blb\x03" {
		return 0, nil, nil, nil, fmt.Errorf("not a Blb3 file: %s sig=%q", path, sig)
	}

	sizeU32, err := br.ReadU32()
	if err != nil {
		return 0, nil, nil, nil, err
	}

	outerFlag, err = br.ReadU32()
	if err != nil {
		return 0, nil, nil, nil, err
	}

	headerKey, err = br.ReadBytes(16)
	if err != nil {
		return 0, nil, nil, nil, err
	}

	headerEnc, err = br.ReadBytes(int(sizeU32))
	if err != nil {
		return 0, nil, nil, nil, err
	}

	headerPlain = append([]byte(nil), headerEnc...)
	Decrypt(headerKey, headerPlain)

	return outerFlag, headerKey, headerEnc, headerPlain, nil
}

